def resolve_level(raw: str) -> str | None:
    stripped = raw.strip()
    lower = stripped.lower()
    return _INPUT_MAP.get(lower) or _INPUT_MAP.get(stripped)


def display_level(level: str) -> str:
    return _DISPLAY.get(level, level)
